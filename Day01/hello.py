# Hello World
print("Hello, World!")

# 简单的变量使用
name = "Python学习者"
print(f"欢迎你，{name}!")

# Python作为计算器
print("\n--- 基础运算 ---")
print(f"1 + 1 = {1 + 1}")
print(f"10 - 3 = {10 - 3}")
print(f"4 * 5 = {4 * 5}")
print(f"10 / 3 = {10 / 3}")
print(f"10 // 3 = {10 // 3}")  # 整除
print(f"10 % 3 = {10 % 3}")    # 取余
print(f"2 ** 3 = {2 ** 3}")    # 幂运算

# 类型转换
print("\n--- 类型转换 ---")
print(f"int('123') = {int('123')}")
print(f"float('3.14') = {float('3.14')}")
print(f"str(100) = {str(100)}")
